def _make_mock_lookup_response(json_data=None):
    """Return a MagicMock for a successful httpx response.

    Uses MagicMock instead of httpx.Response because httpx ≥0.24 raise_for_status()
    raises RuntimeError when the response has no request attribute set.
    """
