async def test_add_word_success_without_sentence(aw_client):
    """AW-P1-01: POST /add-word without sentence — uses LDOCE example as sentence field.

    Acceptance Criterion: Returns 200 with note_id. When no user sentence is
    provided, the LDOCE example from the selected sense is used as the sentence.
    """
