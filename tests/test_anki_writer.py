@pytest.mark.asyncio
async def test_add_word_success(aw_client):
    """AW-P0-01: POST /add-word — success, returns note_id.

    Acceptance Criterion:
      Returns 200 with note_id, sense_used (0), word (lowercased), definition.
      Word is normalised to lowercase before lookup.
    """
    ac, mock_ldoce5 = aw_client
    mock_ldoce5.get.return_value = _make_mock_lookup_response()

    with patch("anki_writer._add_note_and_sync", return_value=99999):
        resp = await ac.post(
            "/add-word",
            json={"word": "Happy", "sentence": "I am very happy today."},
        )

    assert resp.status_code == 200
    data = resp.json()
    assert data["word"] == "happy"      # normalised to lowercase
    assert "note_id" in data
    assert data["note_id"] == 99999
    assert "sense_used" in data
    assert data["sense_used"] == 0      # first (only) sense selected
    assert "definition" in data
    assert data["definition"] != ""


@pytest.mark.asyncio
async def test_add_word_duplicate_returns_422(aw_client):
    """AW-P0-02: POST /add-word — duplicate word rejected.

    Acceptance Criterion: Returns 422 with "duplicate:" in detail.
    """
    ac, mock_ldoce5 = aw_client
    mock_ldoce5.get.return_value = _make_mock_lookup_response()

    with patch(
        "anki_writer._add_note_and_sync",
        side_effect=ValueError(
            "duplicate: cannot create note because it is a duplicate"
        ),
    ):
        resp = await ac.post("/add-word", json={"word": "happy"})

    assert resp.status_code == 422
    assert "duplicate" in resp.json()["detail"]


@pytest.mark.asyncio
async def test_add_word_empty_word_returns_422(aw_client):
    """AW-P0-03: POST /add-word — empty word rejected at validation layer.

    Acceptance Criterion: Returns 422 with "invalid:" in detail.
    No external calls should be made (validation short-circuits before ldoce5-api call).
    """
    ac, mock_ldoce5 = aw_client

    resp = await ac.post("/add-word", json={"word": ""})

    assert resp.status_code == 422
    assert "invalid" in resp.json()["detail"]
    mock_ldoce5.get.assert_not_called()  # ldoce5-api not contacted for invalid input


@pytest.mark.asyncio
async def test_health_returns_required_fields_collection_accessible(aw_client):
    """AW-P0-04: GET /health — returns collection_accessible: true when file exists.

    Acceptance Criterion:
      Returns status: ok, collection_accessible: bool.
      Tests the 'accessible = true' path using a temporary file as collection.
    """
    ac, mock_ldoce5 = aw_client
    mock_ldoce5.get.return_value = Response(200, json={"status": "ok"})

    # Create a temp file to simulate an accessible Anki collection
    with tempfile.NamedTemporaryFile(suffix=".anki2", delete=False) as tmp:
        tmp_path = tmp.name

    try:
        with patch.object(anki_writer, "COLLECTION_PATH", tmp_path):
            resp = await ac.get("/health")

        assert resp.status_code == 200
        data = resp.json()
        assert data["status"] == "ok"
        assert "collection_accessible" in data
        assert data["collection_accessible"] is True
        assert "ldoce5_api_reachable" in data
        assert isinstance(data["ldoce5_api_reachable"], bool)
        assert "last_sync" in data
    finally:
        os.unlink(tmp_path)
