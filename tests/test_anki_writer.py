def _make_mock_lookup_response():
    """Return a MagicMock mimicking a successful httpx response for /lookup.

    httpx.Response(200, json=...) fails in tests because raise_for_status()
    requires self.request to be set (httpx ≥0.24 raises RuntimeError otherwise).
    A MagicMock sidesteps this: raise_for_status() is a no-op MagicMock and
    .json() returns the real dict.
    """
    mock_resp = MagicMock()
    mock_resp.status_code = 200
    mock_resp.raise_for_status = MagicMock()
    mock_resp.json.return_value = SAMPLE_LDOCE5_RESPONSE
    mock_resp.content = b"fake_audio_bytes"
    return mock_resp


